    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        if (filltablenormal) {
            currentPage++;
            HoaDonModel.setRowCount(0);
            int startRow = (currentPage - 1) * rowsPerPage;
            lhd = hds.dataHoaDonPhanTrang(startRow, rowsPerPage);
            fillToTable();
            lblPage.setText(currentPage + "/" + page + "");
            if (currentPage == page) {
                btnNext.setVisible(false);
            } else {
                btnNext.setVisible(true);
            }
        }
        if (timkiem) {
            currentPage++;
            HoaDonModel.setRowCount(0);
            int startRow = (currentPage - 1) * rowsPerPage;
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            String timeBatDau = dateFormat.format(jdNgayBatDau.getDate());
            String timeKetThuc = dateFormat.format(jdNgayKetThuc.getDate());
            lhd = hds.showDataHoaDonQLHoaDonFollowDateTime(timeBatDau, timeKetThuc, startRow, rowsPerPage);
            fillToTable();
            lblPage.setText("");
        }

    }//GEN-LAST:event_btnNextActionPerformed

    private void btnPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrevActionPerformed
        if (filltablenormal) {
            if (currentPage > 1) {
                currentPage--;
                int startRow = (currentPage - 1) * rowsPerPage;
                lhd = hds.dataHoaDonPhanTrang(startRow, rowsPerPage);
                fillToTable();
                lblPage.setText(currentPage + "/" + page + "");
                if (currentPage != page) {
                    btnNext.setVisible(true);
                }
            }
        }
        if (timkiem) {
            if (currentPage > 1) {
                currentPage--;
                int startRow = (currentPage - 1) * rowsPerPage;
                DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
                String timeBatDau = dateFormat.format(jdNgayBatDau.getDate());
                String timeKetThuc = dateFormat.format(jdNgayKetThuc.getDate());
                lhd = hds.showDataHoaDonQLHoaDonFollowDateTime(timeBatDau, timeKetThuc, startRow, rowsPerPage);
                fillToTable();
                lblPage.setText("");
                if (currentPage != page) {
                    btnNext.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_btnPrevActionPerformed

    private void btnTimKiemTheoNgayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemTheoNgayActionPerformed
        if (jdNgayBatDau.getDate() == null || jdNgayKetThuc.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Không được bỏ trống các trường thời gian");
            return;
        }

        currentPage = 1;
        lblPage.setText("");
        rowsPerPage = 5;
        filltablenormal = false;
        timkiem = true;
        int startRow = (currentPage - 1) * rowsPerPage;

        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        String timeBatDau = dateFormat.format(jdNgayBatDau.getDate());
        String timeKetThuc = dateFormat.format(jdNgayKetThuc.getDate());
        if (timeBatDau.equals(timeKetThuc)) {
            System.out.println("Ngày bắt đầu bằng ngày kết thúc.");
        } else if (timeBatDau.compareTo(timeKetThuc) < 0) {
            lhd = hds.showDataHoaDonQLHoaDonFollowDateTime(timeBatDau, timeKetThuc, startRow, rowsPerPage);
            fillToTable();
        } else {
            JOptionPane.showMessageDialog(this, "Ngày bắt đầu sau ngày kết thúc.");
        }

    }//GEN-LAST:event_btnTimKiemTheoNgayActionPerformed

