    private void cboPhuongThucThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboPhuongThucThanhToanActionPerformed
        String pttt = (String) cboPhuongThucThanhToan.getSelectedItem();
        if (pttt.equalsIgnoreCase("Tiền Mặt")) {
            lhd = hds.showDataHoaDonQLHoaDonFollowPTTT("PhuongThuc1");
            jpPhanTrang.setVisible(false);
            fillToTable();
        }
        if (pttt.equalsIgnoreCase("Chuyển khoản")) {
            lhd = hds.showDataHoaDonQLHoaDonFollowPTTT("PhuongThuc2");
            jpPhanTrang.setVisible(false);
            fillToTable();
        }
        if (pttt.equalsIgnoreCase("quẹt thẻ")) {
            lhd = hds.showDataHoaDonQLHoaDonFollowPTTT("PhuongThuc3");
            jpPhanTrang.setVisible(false);
            fillToTable();
        }
        if (pttt.equals("")) {
            jpPhanTrang.setVisible(true);
            currentPage = 1;
            rowsPerPage = 5;
            filltablenormal = true;
            timkiem = false;
            lblPage.setText(currentPage + "/" + page + "");
            int startRow = (currentPage - 1) * rowsPerPage;
            lhd = hds.dataHoaDonPhanTrang(startRow, rowsPerPage);
            if (currentPage != page) {
                btnNext.setVisible(true);
            }
            fillToTable();
        }
    }//GEN-LAST:event_cboPhuongThucThanhToanActionPerformed

