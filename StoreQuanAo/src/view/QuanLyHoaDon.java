    private void fillToTable() {
        HoaDonModel.setRowCount(0);
        for (HoaDon i : lhd) {
            DecimalFormat decimalFormat = new DecimalFormat("###,### VND");
            String formattedAmount = decimalFormat.format(i.getTongTien());
            HoaDonModel.addRow(new Object[]{i.getHoaDonId(), i.getTenPhuongThuc(), i.getNguoiTao(), formattedAmount, i.getHoTenKhachHang(), i.isTrangThai() == true ? "Đã thanh toán" : "Chưa thanh toán", i.getCreated_at()});
        }

    }

    private void fillToTableDonHang() {
        DonHangModel.setRowCount(0);

        for (DonHangSanPhamHoanChinh i : ldhsphc) {
                        DecimalFormat decimalFormat = new DecimalFormat("###,### VND");
            String formattedAmount = decimalFormat.format(i.getGiaTien());
            String thanhtien = decimalFormat.format(i.getGiaTien() * i.getSoLuong());
            
            DonHangModel.addRow(new Object[]{i.getSanPhamSanPhamChiTietId(), i.getTenSanPham(), i.getMausac(), i.getChatlieu(), i.getKichThuoc(), i.getSoLuong(), formattedAmount, thanhtien});

        }
    }

    public void computedPagination() {

        if (hds.countDataHoaDon() % rowsPerPage == 0) {
            page = hds.countDataHoaDon() / rowsPerPage;
        } else {
            page = (int) Math.ceil((double) hds.countDataHoaDon() / rowsPerPage);
        }

    }
